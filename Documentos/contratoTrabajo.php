  <p>
    en adelante “El Trabajador”, se ha convenido en el siguiente contrato de
    trabajo de temporada o de faena transitoria, de acuerdo con los
    preceptos del Código del Trabajo, además, se parte integra de este
    contrato el Reglamento Interno de Orden, Higiene y Seguridad. El
    trabajador autoriza al empleador a enviarle información o comunicarle a
    través de su correo electrónico personal antes detallado
  </p>

  <p>
    <span>PRIMERO:</span> El Empleador contrata al Trabajador para
    desempeñar las labores de <span class="cargoTrabajador"></span> , lo que
    comprende todas las labores inherentes a dichas funciones, así como el
    desarrollo de todos los proyectos o negocios que el Empleador le
    encomiende <br />
    El Trabajador desempeñará sus funciones en
    <span class="lugarTrabajador"></span> pudiendo ser trasladado a cualquier parte
    del país a desarrollar labores similares, por causa justificada y sin
    que ello signifique menoscabo para el trabajador.
  </p>

  <p>
    <span>SEGUNDO: </span> La jornada ordinaria de trabajo será de 45 horas
    semanales distribuida de la siguiente manera:
    <span class="horarioTrabajador"></span>
    con una hora de colación. El tiempo destinado a colación no se
    considerará trabajado para enterar la duración de la jornada de trabajo.
    El horario en el cual se otorgue este periodo de colación será flexible,
    debiendo considerar las necesidades del servicio.
  </p>

  <p>
    <span>TERCERO:</span> La remuneración del Trabajador estará constituida
    y se pagará de la siguiente forma: Sueldo base bruto diario, ascendente
    a la cantidad de $ _____________________________________________________,
    que se pagará por día efectivamente trabajado. Esta remuneración
    se pagará en dinero en efectivo, cheque nominativo o transferencia
    bancaria. El Trabajador autoriza expresamente al Empleador para que se
    le descuente de sus remuneraciones el tiempo no trabajado debido a
    atrasos, inasistencias y permisos. De las cantidades indicadas en los
    párrafos anteriores, el Empleador descontará los montos correspondientes
    a cotizaciones de seguridad social e impuestos.
  </p>

  <p>
    <span>CUARTO:</span> Cuando por necesidades de funcionamiento de la
    Empresa, sea necesario pactar trabajo en tiempo extraordinario, el
    trabajador que lo acuerde, se obligará a cumplir el horario que al
    efecto determinen con la Empleadora, dentro de los límites legales.
    Dicho acuerdo constará por escrito y se firmará por ambas partes,
    previamente a la realización del trabajo.
  </p>
  <p>
    <span>QUINTO:</span> Queda prohibido expresamente al trabajador laborar
    sobretiempo o simplemente permanecer en el recinto de la Empresa,
    después de la hora diaria de salida, salvo en los casos a que se refiere
    el inciso precedente.
  </p>
  <p>
    <span>SEXTO:</span> De acuerdo con el artículo 38, Nº 3 del Código del
    Trabajo, las partes dejan establecido que la jornada ordinaria puede
    comprender domingos o festivos, atendido el carácter de las labores
    frutícolas materia del presente contrato.
  </p>
  <p>
    <span>SEPTIMO:</span>
    Se deja constancia que el trabajador de conformidad con lo dispuesto en
    el artículo 305 del Código del Trabajo, por la naturaleza de las faenas
    no está habilitado para negociar colectivamente.
  </p>
  <p>
    <span>OCTAVO:</span> El trabajador declara estar afiliado al nuevo
    antiguo Sistema Previsional. Por consiguiente, solicita que sus
    cotizaciones previsionales sean enteradas donde corresponda y, asimismo,
    solicita que sus cotizaciones relativas a la salud también sean
    enteradas don corresponda.
  </p>

  <p>
    <span>NOVENO:</span> El presente contrato se entenderá terminado
    automáticamente, en la fecha en que concluyen las faenas que le dieron
    origen y a que se ha hecho referencia en la cláusula primera, sin
    perjuicio que el empleador comunique por escrito este hecho, de
    conformidad con lo dispuesto en el artículo 162 del Código del Trabajo.
  </p>

  <p>
    <span>DECIMO:</span> Para todos los efectos derivados del presente
    contrato las partes fijan domicilio en la ciudad de Talca y se someten a
    la Jurisdicción de sus Tribunales. El presente contrato se firma en 2
    ejemplares, de igual tenor y fecha, quedando dos de ellos en poder del
    Empleador y uno en poder del Trabajador.
  </p>

